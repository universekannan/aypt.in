;(function($,doc,win) 
{
	"use strict";
	
	var PBSocialIcon=function(object,option)
	{
		/**********************************************************************/
		
		var $this=$(object);
		var $option=option;

		/**********************************************************************/

		this.build=function() 
		{
			var helper=new PBHelper();
			
			helper.hover($this.find('a'),$option);
		};
		
		/**********************************************************************/
	}
	
	/**************************************************************************/
	
	$.fn.PBSocialIcon=function(option) 
	{
		var object=new PBSocialIcon(this,option);
		object.build();
	};
	
	/**************************************************************************/

})(jQuery,document,window);

